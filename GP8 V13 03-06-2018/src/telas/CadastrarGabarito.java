    public void verificarCampos(int posicao, JTextField tf) {

        if (tf.getText().isEmpty()) {
            gabarito.getResposta()[posicao] = "nula";
        } else {
            gabarito.getResposta()[posicao] = tf.getText().toUpperCase();
        }

    }

