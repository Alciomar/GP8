    private void SalvarGabaritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarGabaritoActionPerformed

        preencherArray();
        gabarito.setNomeGabarito(tfNomeGabarito.getText());
        gabarito.setRespostaJSON(json.toJson(gabarito.getResposta()));
        if (gabarito.getId() == 0) {
            dao.salvarGabarito(gabarito);
            JOptionPane.showMessageDialog(null, "Gabarito Cadastrado com Sucesso!");
        } else {
            dao.editarUsuario(gabarito);
            JOptionPane.showMessageDialog(null, "Gabarito Editado com Sucesso!");
        }
        limparCampos();
    }//GEN-LAST:event_SalvarGabaritoActionPerformed

