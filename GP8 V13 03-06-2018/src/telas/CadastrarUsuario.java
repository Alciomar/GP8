    private void SalvarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarUsuarioActionPerformed

        if (!tfNome.getText().equals("") && !tfLogin.getText().equals("")
                && !tfSenha.getText().equals("") && !cargo.getSelectedItem().equals("")) {
            usuario.setLogin(tfLogin.getText());
            usuario.setNome(tfNome.getText());
            usuario.setSenha(tfSenha.getText());
            usuario.setCargo(String.valueOf(cargo.getSelectedItem()));
            if (usuario.getId() != 0) {
                dao.editarUsuario(usuario);
                JOptionPane.showMessageDialog(null, "Usuário editado com sucesso!");
            } else {
                dao.salvarUsuario(usuario);
                JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso!");
            }
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Existem campos obrigatórios que não foram preenchidos!");
        }
    }//GEN-LAST:event_SalvarUsuarioActionPerformed

